/** Featured items for the landing page: latest from flagship and economic forecasts. */
export function getFeaturedKnowledgeProducts(n = 3) {
  const combined = [
    ...FLAGSHIP_ITEMS.slice(0, 2),
    ...FORECASTS_ITEMS.slice(0, 2),
  ];
  return combined
    .sort((a, b) => (b.date > a.date ? 1 : -1))
    .slice(0, n)
    .map((item, idx) => ({ ...item, featuredOrder: idx }));
}
